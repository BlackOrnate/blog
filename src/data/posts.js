// ── Helper functions ──────────────────────────────────

const postMap = Object.fromEntries(posts.map((p) => [String(p.id), p]))

export function getPost(id) {
  return postMap[String(id)] || null
}

export function getPostsByColumn(columnId) {
  return posts
    .filter((p) => p.column === columnId)
    .sort((a, b) => a.order - b.order)
}

export function getAdjacentPosts(post) {
  const siblings = getPostsByColumn(post.column)
  const idx = siblings.findIndex((p) => p.id === post.id)
  return {
    prev: idx > 0 ? siblings[idx - 1] : null,
    next: idx < siblings.length - 1 ? siblings[idx + 1] : null,
  }
}

export function getRecentPosts(n = 10) {
  return [...posts].sort((a, b) => new Date(b.date) - new Date(a.date)).slice(0, n)
}

export function searchPosts(query) {
  const q = query.toLowerCase().trim()
  if (!q) return []
  return posts.filter((p) => p.title.toLowerCase().includes(q))
}
